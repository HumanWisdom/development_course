  title=" Understanding the pain of loss  "
  toc="sorrow/s116001"
  transcriptPage="sorrow/s116053t"
  userId:any
  saveUsername=JSON.parse(localStorage.getItem("saveUsername"))
  screenType=localStorage.getItem("audio")
  moduleId=localStorage.getItem("moduleId")
  screenNumber=116053
  startTime:any
  endTime:any
