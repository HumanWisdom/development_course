                <h4 class="mtb0px blog_desc">
                  
                By Dominic Curran, Head of Education, HappierMe </h4>

                <h4 class="mtb0px blog_desc">
                The purpose of this article is to ask questions of our global education system, to create a discussion and to look at education with fresh eyes.                </h4>

                <h4 class="mtb0px blog_desc">
                  
                The world is changing rapidly, at a pace we are struggling to keep up with. Our education system is at a standstill, based as it is in the era of the industrial revolution. We are in the age of the technological revolution, a communication revolution, and an information revolution.                </h4>

                <h4 class="mtb0px blog_desc">
                  
                Education is supposed to prepare children for life ahead — to be happy and find a good job — that is what young people want. In both areas we are failing students — we teach them very little about being mentally healthy, or happy, or the art of successful relationships, and we do not teach them how to be emotionally intelligent — which plays a huge role in their future success.
                </h4>

                <h4 class="mtb0px blog_desc">
                  
                Wisdom has never been more needed to address the challenges the world faces. We can begin by asking questions, especially those we usually don’t ask.                </h4>

                <h4 class="mtb0px blog_sub_title">
                Have we got education upside down?
                </h4>

                <h4 class="mtb0px blog_desc">
                The root of the word ‘education’ is ‘educere’ which means ‘to draw out.’                </h4>

                <h4 class="mtb0px blog_desc">
                Do we believe our children come into the world as empty vessels waiting to be filled with knowledge, skills and understanding? Or do they come into the world with built in abilities; innate curiosity, creativity, well-being, resilience, intelligence, and happiness? It is the purpose of education to draw out these innate qualities each student has.
                </h4>

                

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  Watch children as they look at the world around them.
                  </span>
                 
                  
                
               
                  Everything is new, all is to be explored. A child taking their first steps does not give up after the first attempt. Falling down after each attempt is not labelled as a failure. The same child does not look at other toddlers taking their first steps and think how much better the other children are at walking. Parents do not judge their child for the first failures, or even consider them a failure. It is part of the natural process of learning that is innate in every child.

                  
                </h4>

                

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  Watch the children in a Nursery as they play
                  </span>
                  imaginatively without consideration for how they might look to others. The imagination they are born with runs wild as they express themselves freely. A child who paints their first picture is not assessed or judged then given a label as a ‘good’ or ‘bad’ artist. They are celebrated for using the qualities within them and encouraged to keep going. But this raises another question:

                </h4>

               

               

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  Does our school system educate these qualities into our children or out of them?
                  </span>
                  As our children move on from early years education we begin to give less importance to these innate qualities in favour of an ever narrowing range of subjects. We begin the measuring, labelling, comparing and judging process that is built into our system. We look at a narrow range of abilities such as academic subjects and the children are shown that if they can meet the expectations of the teachers in that narrow range of subjects, or do well in exams, then they will be a ‘success.’ If they don’t, they will be a ‘failure’.

                </h4>

                
                

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  What do we mean by that word success?
                  </span>
                  We live in a world where you are successful if you are better than the person next to you - a world of competition and a race to the top rather than collaboration for the benefit of all. The problems of the world like climate change, war, and diminishing resources are not going to be solved by competition but by genuine collaboration. Should schools, colleges and universities be competing with each other to attain a better set of exam results, and try and get the ‘best’ students so their grades will be higher? Or, should they focus on getting each child to flourish in their own way, and realise their innate potential?

                </h4>

                
                

                

                

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  How much do grades contribute to success?
                  </span>
                  Our education system is focused on grades because they are easy to measure, but research has shown that grades are a poor predictor of long term success. 
                  <span class="blog_highlight_black">
                  What has been shown to be far more important is emotional intelligence, resilience, the ability to solve problems, have healthy relationships, collaborate with others, and manage your own mental health.
                  </span>
                  These qualities are rarely taught through our education system, and because they cannot be easily measured, are not given much importance. Our mission at HumanWisdom is to redress this imbalance and give students and teachers the tools they need to acquire these skills for themselves. In addition to contributing to long term success, they also contribute to long term happiness.

                </h4>


                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  We have an education system that measures children, 
                  </span>
                  compares them, then places them in a band of below average, average or above average. A system that judges them based on those measurements and tells them if they are successful or failures. We do this because we think knowledge is important. It is, but is it important to memorise it and test students on that ability, when it is all easily accessible on the phone or computer anyway?

                </h4>
                <h4 class="mtb0px blog_desc">
                  
                By giving them so much information to assimilate and memorise are we destroying their own curiosity and their love for learning?

                </h4>
                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  When we measure, compare, label and judge our children are we teaching them to do compare themselves with others?
                  </span>
                  Are we inadvertently contributing to problems linked to comparison like mental health issues, stress, anxiety, body image problems, conflict, and so on?

                </h4>
                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  Do the labels we place on our children lead them to believe they are limited, 
                                </span>
                                and have nothing unique to offer the world? Does it encourage them to follow a path in life that is not their true calling, but based on fear, or what others expect of them?

                </h4>
                <h4 class="mtb0px blog_desc">
                  
                Do we then apply the same principles of measuring, comparing, labelling and judging to our teachers and to the institutions in which they serve? 
                <span class="blog_highlight_black">
                Are the labels and judgements we place on our schools and teachers enabling them to make the difference they want to make in the lives of our children,
                  </span>
            </h4>
            <h4 class="mtb0px blog_desc">
                  
            or is it creating a narrow curriculum which is disempowering teachers and leading to frustration, stress, burnout and a disenfranchised profession?
                 
              </h4>
   
              <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  Is there an over reliance on a knowledge based curriculum in a world where information is available literally at our fingertips?                                 </span>
                  Knowledge plays its part. But the biggest leaps of mankind have come from the innate wisdom and genius that is within us. Helping children tap into their innate wisdom should be the real purpose of education. This wisdom comes from a deeper self-understanding and brings creativity, intelligence and curiosity to the forefront and enables us to live with joy, and find solutions to our problems.

                </h4>
                <h4 class="mtb0px blog_desc">
                  
                If our current model of education is the cause of these problems, then it could also be the solution.                
                <span class="blog_highlight_black">
                At present we teach children merely about the world around them, but not about themselves.                   </span>
                As a result, they may know all about chemistry or maths, but do not know how to make good decisions for their lives, deal with stress and anxiety, have healthy relationships, avoid addiction, be kind, or live with a sense of peace.
                <span class="blog_highlight_black">
                By including self-knowledge into the curriculum children would be able to access their own wisdom or inner intelligence 
                </span>
                and have the tools to be successful as human beings, and also be successful in the world.

            </h4>

            <h4 class="mtb0px blog_desc">
                  
            It is time for a fresh approach where knowledge has its place, but memorising and repeating it is less important. Instead                 <span class="blog_highlight_black">
            we need to nurture the innate curiosity and love for learning which children already have. This learning needs to be about themselves, as well as the world around them.
            </span>
            </h4>

            <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                  If we are to change the future of humanity, we need to begin by having a fresh approach to education.                                </span>
                  We need to change education so it no longer adds to the stress and anxiety young people feel, but helps them be happy, mentally and physically healthy, and have the wisdom and skills to deal with the many challenges the coming century will bring. This is what we are trying to do with the Human Wisdom Project. To find out more listen to this podcast 
                </h4>

