                <h4 class="mtb0px blog_desc">
                  The numbers conceal the suffering of the victims of addiction and the anguish of their loved ones. 100,000 people died of a drug overdose last year in the US, and countless more in the rest of the world. The WHO estimates that drug addiction affects 38 million people worldwide, and alcoholism 380 million. Cocaine is a $130 billion industry in Europe alone, and has flooded the cities. A US congressional committee put the annual cost of opioid addiction at $1.5 trillion. In some cities, cocaine is quicker to order than a pizza. Police chiefs admit the war on drugs has been lost, but we still persist in thinking that if somehow we could cut of the supply of drugs we could get rid of the problem. Did we learn nothing from prohibition?
                </h4>

                <h4 class="mtb0px blog_desc">
                  What if there was another way, rooted in learning, that could reduce the need to consume illicit drugs?
                </h4>

                <h4 class="mtb0px blog_desc">
                  We rarely talk about prevention. Imagine if there was a simple solution, rooted in learning about ourselves, which could help prevent this tragedy. Imagine the suffering that could be avoided and the money that could be saved – in policing, prisons, healthcare etc.
                </h4>

                <h4 class="mtb0px blog_desc">
                  In this blog I explore a different approach – using our wisdom to explore what makes the human mind prone to addiction in the first place despite knowing of its dangers; and if that clarity of understanding can help us prevent it, and address the problem at its root. Whether it is drugs, alcohol, gambling or internet addiction – the internal drivers are the same.
                </h4>

                <h4 class="mtb0px blog_desc">
                  There are three common hidden drivers in our thinking that make our mind prone to addiction. They are not right or wrong, but just need to be explored and understood so they do not operate from behind the curtain of our awareness, shaping our behaviour in ways that harm us.
                </h4>

                <h4 class="mtb0px blog_desc">
                  The first of these is our conditioning, or our various influences. If I am part of a group or culture where taking drugs is normal, I begin to do that too, often just to fit in. Or, if I am at a party and a group of people are taking drugs and seem to be having a great time, I want to join in the fun. I may have read some accounts of people who took drugs and said they had a great time, and I feel I want to experience that thrill too. The reason conditioning is so powerful in shaping our behaviour is because we are usually not aware we are being conditioned by our environment, and think the idea of taking drugs, for example, is originally ours. If one is not aware of them, our conditioning influences can be powerful and overwhelm any thought which may whisper that it’s a bad idea to take drugs.
                </h4>

                <h4 class="mtb0px blog_desc">
                  The second driver, and perhaps the most powerful of the three, is our need to escape from our emotional pain. I use that term broadly to refer to any disturbance in our thinking for which there is no easy solution – it includes the ache of feeling less than others; suffering a loss of a job or loved one; the misery of failure; the pain of unrealised desires; a feeling of not being loved and feeling lonely; or the ache of our inner emptiness and boredom. The force behind our need to escape our inner pain is powerful and any thought that says it may not be a good idea to hit the bottle is brushed aside. Much of our sorrow is of our own creation, but that is a subject for another blog.
                </h4>

                <h4 class="mtb0px blog_desc">
                  The third driver is our mind’s need for stimulation and pleasure, as a balm for the emptiness we feel inside. Sometimes we are not aware of that feeling of emptiness because we keep ourselves so busy from morning to night. The modern age offers endless stimulation through the internet, which can also become a habit that is difficult to break. The nature of pleasure is such that when it ends it leaves a void that needs to be filled by yet more pleasure – the same amount won’t do. The one glass of wine a day soon becomes a bottle a day, and before we know it we have developed an alcohol addiction. We boast about our drinking and the fun we had, not pausing to understand that alcohol is a known toxin and linked to so many diseases. We never examine that inner emptiness that drives our need for pleasure and ‘fun’. There is nothing wrong with pleasure by the way, it is an important part of being human.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Having understood all this, what is the next step? How can we understand our conditioning, deal with our emotional pain without escaping from it, and understand pleasure so it has its rightful place in our life? I wish just reading this blog was enough to prevent the scourge of addiction but these drivers in us are powerful and need to be understood as they operate in us. We need to go on a journey of learning about ourselves in a gentle way, with a mind that is open to learning, and not judge ourselves as good or bad, right or wrong. That is why we created the HappierMe app, to give each person the tools to understand themselves deeply, access their own innate wisdom, use that wisdom to meet life’s challenges with serenity, and transform their own life. With this wisdom much of our sorrow can be avoided and more easily dealt with, reducing the need to escape through addiction. The app explores all the subjects dealt with in this article in depth.
                </h4>

                <h4 class="mtb0px blog_desc">
                  If we could bring this understanding to every young person in the world and weave it into education, we could make a significant impact on addiction of all kinds – to food, drink, drugs, gambling, gaming, and so on. Imagine how the world could change if we did that! All we need is to commit ourselves to doing this. After all, 400,000 engineers worked together to put a man on the moon. This is much easier by comparison.
                </h4>

