                <h4 class="mtb0px blog_sub_title">
                1. Why am I looking for love?
                </h4>

                <h4 class="mtb0px blog_desc">
                It may seem a silly question, but an important one nevertheless. Are you looking for love because your heart feels empty and you want someone else to complete you, and make you feel better? No one can fill that emptiness you feel for long. After the first flush of romance wears off the emptiness will return.
                 Will you look for someone new again? How can you be comfortable being alone and love yourself
                  as you are first, before you look for love? This is such an important question to ask ourselves
                   before we begin. It may mean making our peace with our past, letting go of painful memories,
                    and accepting ourselves as we are. It may mean letting go of our ideas of who we should be so
                     we stop feeling disappointed with ourselves. This will boost our self-confidence and help us
                      feel relaxed in our own skin. That is such an attractive quality in a person.</h4>

                      <h4 class="mtb0px blog_sub_title">
                      2. How well do I listen? Do I speak with care?
                      </h4>

                <h4 class="mtb0px blog_desc">
                Do you listen with generosity and without judgment, or with a critical mind? Do you listen to understand, or to interrupt with your own point of view? We are usually poor listeners. Master the art of listening deeply, with attention, with curiosity, and without judgement. Learn to speak with care, pausing to ask yourself how it is going to be received. It is probably the most important skill to learn in any relationship. It can make all the difference to the quality of your relationship, help you avoid conflict, and understand what the other person is saying, or not able to say. It is one of the best ways of showing your love for another person. The depth of a relationship is what really nourishes our spirit and good communication is a crucial way of deepening that bond.

                ‘There is only one happiness in this world. To love and be loved’ – George Sand</h4>

                <h4 class="mtb0px blog_sub_title">
                3. How attached am I to my own point of view?

                 </h4>

                <h4 class="mtb0px blog_desc">
                If we are not aware of it, we become automatically attached to our opinions, beliefs and narratives of how we see the world. We usually want others to agree with us and feel good when they do. We are automatically critical of people who are different to us. All this is linked to our past conditioning influences. We are not aware we are conditioned and yet become attached to it. This results in a power-play and conflict in a relationship – with each person trying to get the other to agree with their point of view. Waking up to this hidden process in ourselves and others allows us to be curious about our differences rather than get upset by them. It is okay for others to be different – in their tastes, opinions and beliefs. Understanding all this allows us to accept others as they are, which is the foundation of any relationship. Some people do not wake up to this process of conditioning operating in the background all their lives and their relationships stumble from one conflict to another. Love vanishes, leaving people confused about where it all went wrong.

               ‘Ego judges and punishes. Loves forgives and heals’ - Anon</h4>





               <h4 class="mtb0px blog_sub_title">
               4. How often do I think about others?

                 </h4>

                <h4 class="mtb0px blog_desc">
                Try this exercise for yourself. Sit down with a pen and paper and write down every 
                thought that comes to your mind for about 10 minutes. Do not try to judge the thoughts,
                 or change them. Just write them as they come up.</h4>
                


                 <h4 class="mtb0px blog_desc">
                 Now look over them. How many of them are linked to yourself? And how many reflect a 
                 concern for someone else?.</h4>



                 <h4 class="mtb0px blog_desc">
                 If we look carefully we spend most of the day thinking about ourselves. In a relationship that can be a challenge.
                  Once the romantic phase passes and if we continue to do so, 
                  the relationship with wither, due to a lack of care. We need to 
                  consciously remind ourselves to make space in our mind for others we are in 
                  a relationship with, be sensitive to their needs, and find ways to be kind every day.
                   It is this daily kindness, care, and thoughtfulness that lays the foundation for a 
                   great relationship.</h4>

                  <h4 class="mtb0px blog_sub_title">
               5. How well do I understand my emotional needs?

                 </h4>

                <h4 class="mtb0px blog_desc">
                We all have a long list of emotional needs we are not aware of, and which we expect 
                others to meet – to be listened to, understood, loved, to feel important, for power, 
                and so on. When these needs are not met we can feel hurt, withdraw our affection or
                 get angry and blame the other person for hurting us. The other person is the same and
                  conflict follows.</h4>
                
                  <h4 class="mtb0px blog_desc">
                  These needs are not right or wrong but need to be understood. Can we take responsibility for these needs, and express them as a request rather than a demand? 
                  How do we respond when we get hurt? A deeper understanding of our own emotional needs can help
                   us to understand the needs others have – and try and meet them.</h4>

