                <h4 class="mtb0px blog_desc">
                  We have all been criticised and
                  <span class="blog_highlight_black">
                    experienced the sudden pain that arises deep within us because of it. We have no control over it.
                  </span>
                  We react to this pain by getting angry, or criticising the other person, or withdrawing our affection, or thinking of other ways of getting our own back, and so on. This is the same in all human beings. Our psychological sense of self, or the ‘I’, which is perhaps a creation of our thinking, experiences pain just like our physical body does, and can feel threatened when criticised.
                </h4>

                <h4 class="mtb0px blog_desc">
                  To understand what is going on, let us look at an example.
                  <span class="blog_highlight_black">
                    If someone is a surgeon and you criticise their abilities as a surgeon, they will feel really bad, but if you tell them that they are a terrible dancer, they might just laugh and agree with you.
                  </span>
                  The difference is that they may have an image or opinion of themselves as a good surgeon, but not as a good dancer. All our images are part of our identity, and when they are challenged, we feel hurt. We are not aware of the many images of ourselves that we have accumulated or how they got there.
                  <span class="blog_highlight_black">
                    The more images we have, the more the risk of getting hurt.
                  </span>
                  Sometimes our life experiences can make us hyper-sensitive, and we can feel threatened even when no criticism was meant.
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    Understanding the mechanism behind the hurt we feel when criticised may allow us to respond with intelligence. All criticism offers us an opportunity to learn about ourselves,
                  </span>
                  even though it’s just another person’s opinion. We may pause, and ask if there is any truth in what is being said, and say sorry or change.
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    If we were smart, we could go further and actively welcome feedback from the people in our lives, 
                  </span>
                  to avoid making mistakes we may not be aware of and to keep our relationships healthy. We may explore our various images of ourselves and ask how we acquired them, and that understanding may allow us to let most of them go. The ability to accept criticism with grace is an important life skill we would all benefit from learning.
                </h4>

                <h4 class="mtb0px blog_desc">
                  This can contribute to our success at home and at work. Many managers complain that employees struggle to accept critical feedback without becoming defensive. The inability to accept criticism and learn from it is one of the reasons many relationships fail because people are scared of saying what they really feel for fear of the other person’s reaction, so bottle it up until it erupts one day.
                </h4>

