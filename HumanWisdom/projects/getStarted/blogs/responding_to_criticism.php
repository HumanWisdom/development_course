                <h4 class="mtb0px blog_desc">
                  Isn’t it astonishing that a few spoken words have such power to hurt us? In this blog, we are going to explore the question of what happens when we are criticised, and how this understanding can help us respond with wisdom rather than react with anger.
                </h4>

                <h4 class="mtb0px blog_desc">
                  We have all been criticised and experienced the sudden pain that arises deep within us because of it. We have no control over it. We react to this pain by getting angry, or criticising the other person, or withdrawing our affection, or thinking of other ways of getting our own back, and so on. This is the same in all human beings.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Our psychological sense of self, which is a creation of our thinking, experiences pain just like our physical body does, and can feel threatened when criticised.
                </h4>

                <h4 class="mtb0px blog_desc">
                  To understand what is going on, let us look at an example. If someone is a surgeon and you criticise their abilities as a surgeon, they will feel really bad, but if you tell them that they are a terrible dancer, they might just laugh and agree with you. The difference is that they may have an image or opinion of themselves as a good surgeon, but not as a good dancer. All our images are part of our identity, and when they are challenged, we feel hurt. We are not aware of the many images of ourselves that we have accumulated or how they got there. The more images we have, the more the risk of getting hurt. Sometimes our life experiences can make us hyper-sensitive, and we can feel threatened even when no criticism was meant.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Understanding the mechanism behind the hurt we feel when criticised may allow us to respond with intelligence. All criticism offers us an opportunity to learn about ourselves, even though it’s just another person’s opinion. We may pause, and ask if there is any truth in what is being said, and say sorry or change. If we were smart, we could go further and actively welcome feedback from the people in our lives, to avoid making mistakes we may not be aware of and to keep our relationships healthy. We may explore our various images of ourselves and ask how we acquired them, and that understanding may allow us to let most of them go. The ability to accept criticism with wisdom and grace is an important life skill we would all benefit from learning. To find our more visit
