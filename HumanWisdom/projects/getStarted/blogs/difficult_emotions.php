                </h4>

                <h4 class="mtb0px blog_sub_title">
                  Step 2 — Awareness
                </h4>

                <h4 class="mtb0px blog_desc">
                  Once the mind feels a bit more calm you need to 
                  <span class="blog_highlight_black">
                    become aware of what is going on around you, in your body, and in your mind.
                  </span>
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    Start with your surroundings 
                  </span>
                  and connect with them to realize that your thoughts and feelings are not your reality. Use all your senses to connect with the present — you could touch something like a table, or smell something like a fruit or some coffee, or listen to some music, or drink some cold water and feel it going down slowly, or just look at the sky and be awed by its vast beauty.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Once you become aware of your surroundings, 
                  <span class="blog_highlight_black">
                    bring this awareness to your body,
                  </span>
                  noticing where you are feeling this emotion — it could be in your chest, stomach , or body. Stay with the sensation of that feeling without naming it if you can.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Use the awareness exercises in the app and notice the world around you and your body freshly.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Once you become aware of your surroundings and your body, it is time to 
                  <span class="blog_highlight_black">
                    become aware of your thoughts and feelings. 
                  </span>
                  There may be dozens of thoughts swirling in our mind in response to what has happened, for example anxiety around sitting for an exam, or if we are criticized, or if we lose someone we love. How can we make sense of all these thoughts and feelings? Many of our emotions don’t have a name either — and can be a mix of different feelings. 
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    This is where journaling can help.
                  </span>
                  Just start writing down all your thoughts and feelings as they arise. Don’t filter them or judge them as right or wrong. As they get out of your head and into the journal you will notice your mind begins to calm down. You can use the online journal in the app to do the journaling.
                </h4>

                <h4 class="mtb0px blog_sub_title">
                  Resources to explore in the HappierMe app:
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/awareness">
                    Awareness,
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/wisdom-exercise/s75002">
                    See the world freshly,
