                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    Feelings are an essential part of being human 
                  </span>
                  and enrich our lives — like falling in love or enjoying the beauty of a spring morning. They can also protect us from harm — like expressing anger when it’s appropriate. Quite often though these feelings can be overwhelming and are behind many of the mental health problems we face.
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    In this article we explore 6 practical steps 
                  </span>
                  we can all follow to feel better now, and then understand where these feelings come from, for long-term benefit. This process is the same no matter what the emotion we may be dealing with. There are also links here to the relevant sections of the 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/">
                    HappierMe app 
                  </a>
                </h4>

                <h4 class="mtb0px blog_desc blog_highlight_black">
                  The HappierMe app supports you to put out the fire in your own mind, and then find out why the fire started in the first place so it does not occur again.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Our mind is an automatic reaction machine and often the same triggers evoke the same powerful feelings in us, which repeat in predictable patterns. We seem powerless to do anything about them. These feelings like anxiety, stress, anger, loneliness, and envy can be overwhelming. These emotions can make us do things we may regret like lashing out and hurting others when we are angry. Or drink too much alcohol or binge eat if we are sad. Or get into unsuitable relationships if we are feeling lonely. Or self-harm if the stress is overwhelming and we can’t think of a way out. They stop us from thinking clearly, reduce our ability to concentrate at work, and impact our performance, so it is worth investing our time and energy to explore better ways of dealing with them.
                </h4>

                <h4 class="mtb0px blog_desc blog_highlight_black">
                  Being able to manage our own emotions can help us manage our own mental health, have happier relationships, avoid addiction and self-harm, and live our best life. This can also help us be emotionally intelligent, which can contribute to our success at work.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Let’s get started.
                </h4>

                <h4 class="mtb0px blog_sub_title">
                  Step 1 — Calm the mind
                </h4>

                <h4 class="mtb0px blog_desc">
                  The first step is to find ways of calming the mind. These feelings are often powerful and you lose your ability to control them, or reason and think your way out of them. Lean into the sensation of the feeling in your body without naming it and 
                  <span class="blog_highlight_black">
                    do a breathing exercise, or go for a walk in nature, or listen to an audio meditation or do a tapping exercise. 
                  </span>
                  They activate the parasympathetic nervous system, slowing our breathing down and reducing the stress response. It is important that you have practiced these beforehand so when the challenge arises you are ready.
                </h4>

                <h4 class="mtb0px blog_desc">
                  <span class="blog_highlight_black">
                    Learn a new skill — to be able to notice a feeling without naming it. 
                  </span>
                  It's a quick way of calming the mind. There is a module called Look without language in the app which can guide you on how to do this.
                </h4>

                <h4 class="mtb0px blog_sub_title">
                  Resources to explore in the HappierMe app:
