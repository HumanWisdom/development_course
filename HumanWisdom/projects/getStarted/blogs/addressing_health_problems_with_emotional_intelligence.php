                <h4 class="mtb0px blog_sub_title">
                  Emotional intelligence can help people self-regulate their emotions, habits and mental health and reduce healthcare costs
                </h4>

                <h4 class="mtb0px blog_desc">
                    <span class="blog_highlight_black">
                      Introduction:
                    </span>
                    Imagine a low-cost solution, rooted in learning, that could empower people to manage their own mental health, self-regulate their habits around food and drink, and reduce the cost of healthcare. Problems which emerge from our thinking can be prevented and more easily resolved if we live with emotional intelligence, with a deeper understanding of ourselves and how our minds work.
                </h4>

                <h4 class="mtb0px blog_desc">
                  Here are some health problems that fall into this category. The global cost of each of them is more than a trillion dollars. The hyperlinks link to the relevant sections of the HappierMe app.
                </h4>

                <h4 class="mtb0px blog_desc">
                  1. 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/dealing-with-depression">
                    Depression:
                  </a>
                  1 in 6 people in the UK experience moderate to severe depression(ONS), and the numbers are higher in the US.
                </h4>

                <h4 class="mtb0px blog_desc">
                  2. 
                  <span class="blog_highlight_black">
                    Teen mental health:
                  </span>
                  1 in 7, 10-19 year olds suffer a mental health problem worldwide (WHO).
                </h4>

                <h4 class="mtb0px blog_desc">
                  3. 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~37.mp3/37/T/Five-ways-to-avoid-stress">
                    Stress:
                  </a>
                  impacts our mental and physical health. An IPSOS poll in 2022 in 34 countries said 60% of people reported feeling stressed to the point they could not cope. The US Surgeon General reports that 76% of employees report a mental health problem. The American institute of stress reported 77% of people experience stress that impacts their physical health. 
                </h4>

                <h4 class="mtb0px blog_desc">
                  4. 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/food-health">
                    Obesity:
                  </a>
                  affects 42% of Americans, 28% of Britons, and 1 billion people worldwide are obese (WHO). In most cases, obesity is a dysfunction of thinking, in our inability to self-regulate.
                </h4>

                <h4 class="mtb0px blog_desc">
                  5. Excess 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~21.mp3/21/T/Avoiding-addiction">
                    alcohol consumption
                  </a>
                  is linked to 200 diseases and causes 3 million deaths worldwide (WHO). This is alo an inability to self-regulate.
                </h4>

                <h4 class="mtb0px blog_desc">
                  6.  
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~11.mp3/11/T/Exploring-addiction">
                    Drug addiction
                  </a>
                  In 2021, 107000 people died of an opioid overdose in the US. 35 million people worldwide suffer from drug use disorders (WHO).
                </h4>

                <h4 class="mtb0px blog_desc">
                  7. 
                  <span class="blog_highlight_black">
                    Smoking:
                  </span>
                  1.3 billion people worldwide smoke and it accounts for 8 million premature deaths per year (WHO).
                </h4>

                <h4 class="mtb0px blog_desc">
                  8.  
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~72.mp3/72/T/Preventing-self-harm-and-suicide">
                    Suicide
                  </a>
                  700,000 people a year take their own lives, and many times as many attempt to do so (WHO). This is preventable.
                </h4>

                <h4 class="mtb0px blog_desc">
                  9. Every physical disease has a
                  <span class="blog_highlight_black">
                    psychological impact
                  </span>
                  which causes so much distress. This distress worsens our overall health. At present modern medicine offers little help for this, focusing on the physical illness instead.
                </h4>

                <h4 class="mtb0px blog_desc">
                  10.  
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/fear-anxiety">
                    Anxiety
                  </a>
                  affects up to 60% of people and is on the increase (MIND). It is a reaction from our mind to external challenges and an attempt to control the future. It can be disabling. 
                </h4>

                <h4 class="mtb0px blog_sub_title">
                  How can
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~52.mp3/52/T/Emotional-intelligence">
                    emotional intelligence
                  </a>
                  help prevent these problems and more easily resolve them? 
                </h4>

                <h4 class="mtb0px blog_desc">
                  1. Our mind assumes that our 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/stress">
                    stress, 
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/fear-anxiety">
                    anxiety,
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/wisdom-shorts/1.37.mp4">
                    unhappiness,
                  </a>
                  and
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~40.mp3/40/T/Overcoming-Depression">
                    depression
                  </a>
                  are caused by external events. We do not realise that our feelings are caused by how our mind reacts to these events, because no two people react in the same way to the same event. Our reactions, which determine our feelings, come from all our accumulated conditioning influences. We are not aware we have been conditioned, and yet become attached to this conditioning - and this shapes our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/opinions-beliefs">
                    opinions,
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/opinions-beliefs">
                    beliefs,
                  </a>
                  the stories we tell ourselves, and our behaviour. Being aware of this process helps us be in charge of how we react, and we can then self-regulate our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/wisdom-shorts/1.70.mp4">
                    emotions.
                  </a>
                </h4>

                <h4 class="mtb0px blog_desc">
                  2. Stress is just the difference between how things are and how we want them to be. To get over stress we can either change the event, or our reaction to it. Emotional intelligence gives us the ability to change how we react, and that can put us in control of our own lives.
                </h4>

                <h4 class="mtb0px blog_desc">
                  3. Besides our conditioning there are many other hidden drivers in our own thinking that contribute to our own stress [See the
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/stress">
                    Causes of stress
                  </a>
                  session in the Stress module in the app]. These include
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/comparison">
                    comparison, 
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/wisdom-shorts/1.77.mp4">
                    fear, 
                  </a>
                  our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/reactive-mind">
                    reactive mind, 
                  </a>
                  our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/external-approval">
                    need for external approval, 
                  </a>
                  our various 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/emotional-needs">
                    emotional needs, 
                  </a>
                  and so on. None of these are right or wrong, but just need to be understood. This understanding does all the work of bringing change. This is not to say that life’s challenges are not real, and do not need addressing. Being less stressed can help us address them much more effectively.
                </h4>

                <h4 class="mtb0px blog_desc">
                  4. Unhappy
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/relationships">
                    relationships
                  </a>
                  are a major cause of poor mental health. A deeper understanding of ourselves helps us to understand others better, and realise that deep down our minds function in similar ways. This leads to empathy and compassion, and can both prevent problems and heal our divisions. 
                </h4>

                <h4 class="mtb0px blog_desc">
                  5. We think obesity, smoking, excess alcohol consumption and psychoactive drug use are different problems, but they have their roots in the same hidden drivers in our thinking - and understanding them can bring behavioural change. More importantly, this can help prevent these problems from taking root.
                </h4>

                <h4 class="mtb0px blog_desc">
                  A. Our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/conditioning">
                    conditioning
                  </a>
                  plays a significant role - if we are in an environment where a lot of people are smoking, or drinking, and seem to be having fun, then we want that for ourselves and never pause to consider the consequences.
                </h4>

                <h4 class="mtb0px blog_desc">
                  B. Our
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/inner-boredom">
                    inner boredom
                  </a>
                  and need for
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/pleasure">
                    pleasure
                  </a>
                  [see app modules for details] are powerful drivers and can drive our behaviour to eat more than we need Jo, or drink in excess, or take drugs. All pleasure is fleeting however, and then the mind needs more and more for the same effect - and that is how addiction develops. Through
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/daily-practise/0">
                    breathing exercises
                  </a>
                  and a deeper self-understanding we can better self-regulate our behaviour and
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~24.mp3/24/T/Living-with-peace">
                    be at peace
                  </a>
                  with ourselves
                </h4>

                <h4 class="mtb0px blog_desc">
                  C. Many of our behaviours are driven by our inability to understand and process our emotional pain - so numbing it with alcohol or drugs is a tempting option. Emotional intelligence offers the possibility of avoiding emotional pain in the first place, and giving us options for dealing with it in healthier ways [See modules on 
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/habit-addiction">
                    Addiction,
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/emotional-needs">
                    Emotional needs,
                  </a>
                  and the
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~28.mp3/28/T/Healing-Emotional-Pain">
                    podcast on emotional pain
                  </a>
                  ].
                </h4>

                <h4 class="mtb0px blog_desc">
                  6. Dealing with the psychological impact of illness is much easier if one has developed an inner resilience through self-understanding, and understands the nature of fear, and has tools in place to deal with challenges.
                </h4>

                <h4 class="mtb0px blog_desc">
                  7. Suicide prevention: [
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/audiopage/~podcasts~33.mp3/33/T/Preventing-suicide">
                    Listen to our podcast on suicide prevention
                  </a>
                  ]. A deeper self-understanding builds resilience, and is like learning to swim, so when the challenges of life arrive we can swim through them, and not drown.
                </h4>

                <h4 class="mtb0px blog_sub_title">
                  How can we get people to embrace this self-understanding and transform their lives? What will motivate them to do so?
                </h4>

                <h4 class="mtb0px blog_desc">
                  1. This is the challenging part. To do this we created the
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/adult-dashboard">
                    HappierMe app
                  </a>
                  with more than 70 modules, in collaboration with experts from around the world, offering people different ways of engaging with the content. In addition to the program for adults, we are developing one for teenagers. Children as young as 8 grasp this understanding easily - it is simple - but the impact can be profound.
                </h4>

                <h4 class="mtb0px blog_desc">
                  2. We begin where people are and help them deal with whatever challenge they face - relationships, addiction, or mental health, or if they just want to boost their communication skills.
                </h4>

                <h4 class="mtb0px blog_desc">
                  3. Wherever people begin, the app helps them deal with the immediate problem [
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/daily-practise/0">
                    breathing exercises, 
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/wisdom-shorts">
                    videos, 
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/journal">
                    journaling, 
                  </a>
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/guided-meditation">
                    meditations
                  </a>
                  ] and then takes them deeper to understand their own thinking. That deeper understanding does the work of bringing behavioural change.
                </h4>

                <h4 class="mtb0px blog_desc">
                  4. If we can embed this deeper self-understanding as a subject in education worldwide, we can transform healthcare in a generation, by giving people the tools to manage their own mental health, self-regulate their emotions and their behaviour, and
                  <a class="blog_highlight_peach td_underline" href="https://happierme.app/adults/living-with-peace">
                    live in peace
                  </a>
                  with themselves and others. 
                </h4>

                <h4 class="mtb0px blog_desc">
                  5. This entire project is not based on any ideology - but just on looking and learning about ourselves. 
                </h4>

