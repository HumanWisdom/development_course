                <h4 class="mtb0px blog_desc">
                Integrity is the most important quality of leadership.

Having an intention to live with integrity is an important first step, but is not enough. Leaders work under pressure, making many decisions a day. When making a decision we think we are making an informed choice. Our hidden self-interest is always operating in the background though, and it pushes us to do things that are primarily in our own interest, and not in the interest of the country or organization or department we lead. We can justify any decision to ourselves — our mind is very resourceful in that regard. In doing so we may not consider all the consequences of our actions. We may think it does not matter to tell a small lie, or do someone a favour because they have done something for us in the past, or break a rule because it’s a minor infringement and no one will find out, or accept a favour because everyone is doing that, or make a decision that suits our agenda rather than what is best for the country or organization, and so on. We may fool ourselves into thinking that people will not see through our real motives. Leaders live under immense scrutiny and most things eventually come out. Once people think you lack integrity and cannot be trusted on the small decisions, they assume you cannot be trusted on the big decisions either. Leadership is by consent in most cases, and once you have lost that trust, your days as a leader are numbered. Self-interest is part of being human and labelling it as wrong blocks further exploration.</h4>





               

